package youtubeSadiEvrenSeker;

public class If_ElseIf_Else {

	public static void main(String[] args) {

		int i = 15;

		if (i == 10) {
			System.out.println("The value of 'i' is 10");

		} else if (i < 10) {
			System.out.println("The value of 'i' is less than 10");
		} else {
			System.out.println("The value of 'i' is not equal and not less than 10");
		}

	}
}

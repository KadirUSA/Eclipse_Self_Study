package youtubeSadiEvrenSeker;

public class For_If_While_DoWhile {

	public static void main(String[] args) {

		for (int i = 0; i <= 10; i++) {
			System.out.println("Number For: " + i);
			
		}

		int i = 0;
		while(i<=10) {
			System.out.println("Number While: " + i);
			i++;
			
		}
		
		int j = 0;
		do {
			System.out.println("Number Do: " + j);
			j++;
		}
		while (j <= 10);
		
	}
	}
